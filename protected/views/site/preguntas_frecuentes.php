<p>
<h4 style="font-weight: bolder;">¿QUÉ SIGNIFICA EL ESTATUS DE LAS INTENCIONES DE COMPRA?</h4>


Puedes revisar el estatus en el que se encuentra algún pedido ingresando a la sección “Pedidos” en el menú principal como usuario comprador; y en la sección “Órdenes” en el menú principal como usuario vendedor. El significado de cada estado es el siguiente:
<ul>
	<li> * Pendiente: La intención de compra fue enviada al vendedor y está en espera de  revisión.
</li>
	<li> * Rechazada: El vendedor ha cancelado la intención de compra solicitada.</li>
	<li> * Aprobada: El vendedor ha confirmado la intención de compra y está a la espera del pago.</li>
	<li> * Negociado: El vendedor sugiere un cambio en las cantidades de los productos (EN CONSTRUCCIÓN).</li>
</ul>
</p>
