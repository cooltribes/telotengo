<h4 style="font-weight: bolder;">¿POR QUÉ MI PEDIDO HA SIDO RECHAZADO?</h4>

Existen diferentes motivos por los cuales el vendedor puede rechazar un pedido:<br>
<ul>
	<li> * No se cuenta inventario suficiente para cubrir el pedido.</li>
	<li> * Se agotó el inventario por completo.</li>
	<li> * Falta de pago.</li>
	<li> * Solicitud de cancelación del pedido.</li>
</ul>

Si aún deseas los productos que fueron rechazados, deberás realizar un nuevo pedido. En caso de que hayas aprovechado un descuento especial o alguna promoción, desafortunadamente ésta ya no podrá ser aplicada una vez que la orden ha sido cancelada. Si tienes alguna otra duda con respecto a un pedido, por favor no dudes en contactarnos

