<h4 style="font-weight: bolder;">¿COMO CARGAR MI INVENTARIO o PRODUCTO NUEVO? </h4>


Las empresas vendedoras o compra/venta podrán solicitar la creación de nuevos productos en TELOTENGO, dichos productos no se mostrarán en la tienda hasta que hayan sido verificados y aprobados por el Administrador del Sistema. Para ello los usuarios tendrán la opción de crearlos individualmente o a través de una plantilla Master Data.<br>

El procedimiento a seguir es el siguiente:<br>
Mouseover ‘Inventario’<br>
Click en ‘Agregar un producto’<br>
Seleccionar ‘Carga individual’ o ‘Carga masiva’<br>
Carga individual<br>
Primero se presentará la pestaña Información Vital que va a contener aquellos atributos que son generales (para todos los productos), tales como:<br>

<li>   Nombre* </li>
<li>   Marca* </li>
<li>   Modelo* </li>
<li>   Categoría* </li>
<li>   Subcategoría* </li>
<li>   UPC</li>
<li>   EAN </li>
<li>   GTIN </li>
<li>   Número de Parte del Fabricante</li>
<li>   Color*</li>
<li>   Tonalidad</li>

<br>

Al guardar la información anterior se activarán el resto de pestañas para la solicitud de productos. La que continúa es la pestaña Imágenes donde el usuario podrá cargar un máximo de 9. Se podrán arrastrar las imágenes para ordenarlas de acuerdo a cómo se quieran mostrar en la tienda, la primera imagen será la principal.<br>

En la pestaña Características deberá escribir la Descripción del producto y un máximo de 5 características.<br>

●	La pestaña Más Detalles contiene una serie de atributos opcionales, relacionados específicamente con la categoría y subcategoría seleccionadas previamente.<br>

Carga masiva<br>
Descargar el archivo: Se descargará una plantilla de Excel llamada TLT-Masterdata donde el usuario deberá llenar aproximadamente 22 celdas en las cuáles se le solicitará la siguiente información:
<li>UPC</li>
<li>EAN </li>
<li>GTIN </li>
<li>Número de Parte del Fabricante</li>
<li>Nombre* </li>
<li>Marca* </li>
<li>Modelo* </li>
<li>Color*</li>
<li>Descripción</li>
<li>Característica 1</li>
<li>Característica 2</li>
<li>Característica 3</li>
<li>Característica 4</li>
<li>Longitud</li>
<li> Ancho</li>
<li>Altura</li>
<li>Peso</li>
<li>Descripción SEO</li>
<li> Tags</li>
<li>URL Amigable</li>
<br>
NOTAS:
Inicialmente se venderán productos electrónicos, razón por la cual hasta ahora existe solo una plantilla.
Los campos que contienen * son obligatorios.<br>

Paso 1 validar el archivo: para informar los errores cometidos y permitirle al usuario corregirlos antes de que se suba el archivo y llegue la información al administrador.<br>

Paso 2 subir el archivo: se subirá el Excel libre de errores para que sea revisado por el administrador y éste decida si aprueba o rechaza la creación  de cada uno de los productos allí contenidos.<br>

NOTA: el usuario no podrá cargarle inventario al producto solicitado hasta tanto no sea aprobado por el administrador de Telotengo

