<h4 style="font-weight: bolder;">¿QUÉ SIGNIFICA EL ESTATUS DE MIS COMPRAS?</h4>

Puedes revisar el estatus en el que se encuentra tu pedido ingresando a “Mi cuenta” y haciendo clic en la sección “Ordenes”. Conoce el significado de cada estatus en el siguiente listado:<br>
Rechazado: Tu pedido ha sido cancelado. <br>
Pendiente: Supone que están trabajando para determinar si cumplen tu pedido<br>
Aprobado Pago Pendiente: Tu pedido ha sido confirmado y está a la espera de tu pago<br>
Negociado: El proveedor te sugiere un cambio en los productos (EN CONSTRUCCIÓN)<br>
Despacho: Tu pedido ha sido confirmado y está siendo procesado para ser enviado lo más pronto posible. Cuando sea despachado, recibirás una notificación por correo electrónico con más detalles sobre el envío.<br>

¿Resolvimos tu duda? <br>
Si No
