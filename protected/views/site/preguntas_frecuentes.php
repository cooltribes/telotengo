<h4 style="font-weight: bolder;">¿POR QUÉ MI PEDIDO HA SIDO RECHAZADO?</h4>

Existen diferentes motivos por los cuales un pedido puede ser cancelado:<br>
Solicitaste la cancelación de tu pedido.<br>
No cuentan con una gran cantidad de productos.<br>
Deben renegociar el pedido con el proveedor.<br>
Debido a la gran demanda del producto, actualmente nos quedamos sin existencias.<br>
Rechazado por falta de pago<br>
Si aún deseas el producto que fue rechazado deberás realizar un nuevo pedido. En caso de que hayas aprovechado un descuento especial o alguna promoción, desafortunadamente ésta ya no podrá ser respetada una vez que la orden ha sido cancelada. Si necesitas ayuda con tu pedido, por favor no dudes en contactarnos.<br>
