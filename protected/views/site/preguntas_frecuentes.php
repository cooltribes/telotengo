<h4 style="font-weight: bolder;">¿CÓMO CARGAR MI INVENTARIO?</h4>
La carga de inventario parte del hecho de que todos los productos se encuentran registrados en el sistema, en caso de que no se encuentre alguno, debes solicitar la creación del mismo de acuerdo a la pregunta anterior. De lo contrario busca la opción “Inventario” en el menú principal,  selecciona “Cargar” y allí tendrás la opción de cargar inventario de forma individual o masiva.

<ul>

	<li> 1. Carga individual</li>
	<ul> 1.1 El usuario deberá introducir el nombre del producto al que desea cargar   inventario y hacer clic en “Buscar”.</ul>
	<ul> 1.2 Al guardar la información anterior se activarán el resto de pestañas para la solicitud de productos. La que continúa es la pestaña “Imágenes” donde podrás cargar un máximo de 9. Podrás arrastrar las imágenes para ordenarlas de acuerdo a cómo se quieran mostrar en la tienda, la primera imagen será la principal.</ul>
	<ul> 1.3 Al encontrar el producto deseado haz click en el botón ‘Cargar/Actualizar Inventario’. El sistema dirigirá a una página donde debes indicar el almacén donde actualizarás el producto.</ul>
	<ul> 1.4 Seguidamente debes introducir
		<ul>
			<li>- SKU </li>
			<li>- Condición* </li>
			<li>- Costo </li>
			<li>- Precio* </li>
			<li>- IVA </li>
			<li>- Precio con IVA </li>
			<li>- Cantidad* </li>
			<li>- Garantía </li>
		</ul>
	</ul>
	<ul> 1.5 Haz click en “Guardar” y el sistema te dirigirá a la página donde se muestra todo el inventario de tu empresa.</ul>
	<li> 2. Carga masiva</li>
		<ul> 2.1 Descarga el archivo: Se descargará una plantilla de Excel llamada inboundTLT donde deberás llenar 7 celdas en las cuáles se requiere la siguiente información:

		<ul>
			<li>- SKU </li>
			<li>- Condición* </li>
			<li>- Costo </li>
			<li>- Precio* </li>
			<li>- IVA </li>
			<li>- Precio con IVA </li>
			<li>- Cantidad* </li>
			<li>- Garantía </li>
		</ul>
		</ul>
		<li> 2.2 El siguiente paso es validar el archivo: para informar los errores cometidos y permitirte corregirlos antes de que subas el archivo. </li>
		<li> 2.2 Cuando el archivo sea validado exitosamente podrás proceder a subirlo y con ello ya se actualizará tu inventario.</li>
</ul>

