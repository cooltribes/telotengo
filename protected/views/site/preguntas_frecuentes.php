<p>
<h4 style="font-weight: bolder;">¿Cómo Comprar?</h4>
Tu empresa debe estar registrada como: Comprador o Comprador/Vendedor para tener opciones de compra. Si tu empresa no tiene este rango solicitalo en Contacto. Si ya estás registrado como usuario comprador sigue estos pasos:<br>

Encuentra tu producto utilizando las múltiples opciones de búsqueda. En algunos casos, dependiendo del producto, deberás elegir la variación, ya sea por talla, color o medidas.Tambien deberas elegir la cantidad (sea razonable no compre toda la existencia es probable que su pedido sea rechazado)<br>
Haz clic en el botón “Ordenar” para agregar el producto a tu carrito de compras. Puedes continuar con el proceso para finalizar tu reserva de pedido o, si lo deseas, puedes seguir navegando en la página y agregar más productos a tu carrito.
Para finalizar la compra haz clic en el botón “Generar Orden al proveedor”. <br>
¡Felicidades! Has terminado exitosamente tu pedido en Telotengo.com. Tu orden ha sido registrada en el sistema. Una vez que tu pedido sea validado, recibirás un correo electrónico con la confirmación de tu compra.<br>

¿Resolvimos tu duda? <br>
Si No
</p>
