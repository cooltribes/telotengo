<div><strong><h3 style="font-weight: bold;">DERECHOS DE PROPIEDAD INTELECTUAL</h3> </strong></div>
<hr class="no_margin_top">
<p>

En TELOTENGO.COM tenemos el compromiso de respetar y proteger los derechos de propiedad intelectual tanto de los proveedores como de terceros, por lo que contamos con un espacio para que se puedan reportar productos que se venden a través de nuestra página y que el usuario considera que está infringiendo derechos Propiedad de Intelectual.
</p>

<br>
<p>

Nuestro compromiso tiene por objeto permitir a los titulares de derechos de propiedad intelectual (derechos de autor, marcas, patentes, entre otros) que notifiquen a TELOTENGO.COM si creen que sus derechos están siendo vulnerados por la venta de algún  que se ofrecen en el portal. De este modo, TELOTENGO.COM procederá a estudiar cada caso para determinar si los productos deben ser bajados del portal por no contar con las autorizaciones que se requieren, o si por el contrario no se están infringiendo derechos de terceros.
</p>

<br>
<p>

Cualquier persona natural o jurídica que sea titular de un derecho de propiedad intelectual podrá presentar el reclamo cuando considere que con la venta de algún producto ofrecido por medio de nuestra página, se le está vulnerando un derecho. En el caso de personas jurídicas, el reclamo deberá ser presentado por su representante legal o alguna persona autorizada a través de un documento escrito.
</p>

<br>
<p>

Para poder realizar el reclamo, se deberá consignar ante TELOTENGO.COM la titularidad de los Derechos de Propiedad Intelectual que se presumen vulnerados. Con el fin de corroborar lo anterior, se deberá enviar al correo: contacto@telotengo.com la siguiente información: Número de registro, Tipo de derecho (autor, marca, patente, software, etc.), Clase o clases internacionales en la cual se tiene el registro (En caso de registro de marcas), Tipo de marca, País donde se encuentra registrada la marca, Fecha vencimiento del derecho, SKU/referencia del producto que considera le está vulnerando sus derechos, de igual forma deben adjuntar todos los documentos que acrediten la titularidad de los Derechos de Propiedad Intelectual, que hayan sido registrados en el país en el cual se presente la supuesta infracción. En caso de que la persona que interponga el reclamo no sea el titular debe adjuntarse una autorización del titular de los derechos.
</p>

<br>
<p>
Una vez en TELOTENGO.COM recibamos la reclamación, procederemos a dar inicio a la investigación. En caso de no ser procedente, se enviará una respuesta al correo electrónico del usuario, informando los motivos de esta decisión. Si por el contrario, TELOTENGO.COM logra verificar que los productos y publicaciones infringen derechos de terceros, los eliminaremos de nuestra página.</p>
